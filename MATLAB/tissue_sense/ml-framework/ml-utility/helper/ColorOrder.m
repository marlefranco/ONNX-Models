    methods
        function obj = ColorOrder( cmap )
            obj.OriginalMap = cmap;
            obj.ColorMap = cmap;
        end
    end %constructor
    
    methods
        
        function reset(obj)
            obj.ColorMap = obj.OriginalMap;
            
        end
        
        function permute( obj, colorOrder )
            obj.reset();
            obj.ColorMap = obj.ColorMap(colorOrder,:);
        end
        
        function palettes( obj )
            
            colorSchemes = enumeration('viz.ColorOrder');
            
            nScheme = numel( colorSchemes );
            
            figure( "Color", "W" );
            for iScheme = 1 : nScheme
                
                thisAxes = subplot(3,6,iScheme);
                
                colorSchemes( iScheme ).preview( thisAxes );
                
                
            end % for iScheme
            
        end %palettes
       
        
        function preview( obj, hAx )     
            
            
            colorOrder = obj.ColorMap;
            
            if nargin < 2       
                figure();
                hAx = axes();
            end
            
            hAx.NextPlot = 'replacechildren';
            
            for i = 1 : size(colorOrder,1)
                x = [0 1 1 0];
                y = [i-1 i-1 i i];
                patch(x, y, colorOrder(i,:));
            end
            
            hAx.NextPlot = 'add';
            hAx.Visible = 'off';
            
            title(hAx, string(obj) );
            set(findall(gca, 'type', 'text'), 'visible', 'on');
            
        end %function
       
    end %methods
    
    
